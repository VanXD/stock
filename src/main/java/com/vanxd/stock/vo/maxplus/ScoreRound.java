package com.vanxd.stock.vo.maxplus;

public class ScoreRound {
    private String map_name;
    private int ot_type;
    private int team1_score;
    private int team2_score;

    public String getMap_name() {
        return map_name;
    }

    public void setMap_name(String map_name) {
        this.map_name = map_name;
    }

    public int getOt_type() {
        return ot_type;
    }

    public void setOt_type(int ot_type) {
        this.ot_type = ot_type;
    }

    public int getTeam1_score() {
        return team1_score;
    }

    public void setTeam1_score(int team1_score) {
        this.team1_score = team1_score;
    }

    public int getTeam2_score() {
        return team2_score;
    }

    public void setTeam2_score(int team2_score) {
        this.team2_score = team2_score;
    }

}
