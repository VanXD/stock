package com.vanxd.stock.vo.maxplus;

public class TeamInfo {
    private String bg_color_type;
    private int game_team_id;
    private String mmr;
    private String rank;
    private String tag;
    private int team_id;
    private String win_rate;

    public String getBg_color_type() {
        return bg_color_type;
    }

    public void setBg_color_type(String bg_color_type) {
        this.bg_color_type = bg_color_type;
    }

    public int getGame_team_id() {
        return game_team_id;
    }

    public void setGame_team_id(int game_team_id) {
        this.game_team_id = game_team_id;
    }

    public String getMmr() {
        return mmr;
    }

    public void setMmr(String mmr) {
        this.mmr = mmr;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public int getTeam_id() {
        return team_id;
    }

    public void setTeam_id(int team_id) {
        this.team_id = team_id;
    }

    public String getWin_rate() {
        return win_rate;
    }

    public void setWin_rate(String win_rate) {
        this.win_rate = win_rate;
    }
}
